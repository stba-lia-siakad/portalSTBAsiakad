class Beasiswa_model extends CI_Model
{

    public $table = 'tbl_beasiswa';
    public $id_beasiswa = 'id_beasiswa';
    public $order = 'DESC';

    function __construct()
    {
        parent::__construct();
    }

    // get all
    function get_all()
    {
        $this->db->order_by($this->id_beasiswa, $this->order);
        return $this->db->get($this->table)->result();
    }

    // get data by id
    function get_by_id($id_beasiswa)
    {
        $this->db->where($this->id_beasiswa, $id_beasiswa);
        return $this->db->get($this->table)->row();
    }
    
    // get total rows
    function total_rows($q = NULL) {
        $this->db->like('id_beasiswa', $q);
	$this->db->or_like('nama_beasiswa', $q);
	$this->db->or_like('keterangan', $q);
	$this->db->from($this->table);
        return $this->db->count_all_results();
    }

    // get data with limit and search
    function get_limit_data($limit, $start = 0, $q = NULL) {
        $this->db->order_by($this->id_beasiswa, $this->order);
        $this->db->like('id_beasiswa', $q);
	$this->db->or_like('nama_beasiswa', $q);
	$this->db->or_like('keterangan', $q);
	$this->db->limit($limit, $start);
        return $this->db->get($this->table)->result();
    }

    // insert data
    function insert($data)
    {
        $this->db->insert($this->table, $data);
    }

    // update data
    function update($id_beasiswa, $data)
    {
        $this->db->where($this->id_beasiswa, $id_beasiswa);
        $this->db->update($this->table, $data);
    }

    // delete data
    function delete($id_beasiswa)
    {
        $this->db->where($this->id_beasiswa, $id_beasiswa);
        $this->db->delete($this->table);
    }

}

